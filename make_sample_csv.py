# تابع برای تولید شماره موبایل ایرانی تصادفی
def generate_iranian_mobile():
    operators = ["0910", "0911", "0912", "0913", "0914", "0915", "0916", "0917", "0918", "0919",
                "0990", "0991", "0992", "0993", "0994", "0930", "0933", "0935", "0936", "0937", 
                "0938", "0939", "0901", "0902", "0903", "0904", "0905", "0920", "0921", "0922"]
    
    operator = random.choice(operators)
    rest_of_number = ''.join([str(random.randint(0, 9)) for _ in range(7)])
    
    return operator + rest_of_number

# ساخت دیتاست با 1000 سطر
data = []
for _ in range(1000):
    first_name = random.choice(first_names)
    last_name = random.choice(last_names)
    full_name = first_name + " " + last_name
    mobile = generate_iranian_mobile()
    
    data.append([full_name, mobile])

# ایجاد دیتافریم پاندا
df = pd.DataFrame(data, columns=["نام و نام خانوادگی", "شماره موبایل"])

# ذخیره به عنوان فایل اکسل
